# List is a kind of data structure. Can store multiple data type in same list
friends = ["Kevin", "Karen", "Jim", "David", "Oscar"]

# Print entire list
print(friends)
# Normal random access
print(friends[0])
# Reverse random access: show from right-most
print(friends[-1])

# Multiple access
# Select all after
print(friends[1:])
# All in range: Can use out-of-range index too!
print(friends[2:4])

