# Tuple can also store multiple data type like list too
coordinates = (4, 5)

# The tuples is "immutable" - can't be modify at all
print(coordinates[0])

# We usually use tuple for constant data since it can't be change
# Accessing tuple in list
newCoord = [(4, 5), (6, 7), (80, 34)]
print(newCoord[0][1])
